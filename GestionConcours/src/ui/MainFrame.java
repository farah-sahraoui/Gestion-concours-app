    private void btnAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouterActionPerformed
       try {
        // Récupération des données
        String nom = txtNom.getText().trim();
        String email = txtEmail.getText().trim();
        String ville = txtVille.getText().trim();
        String niveau = txtNiveau.getText().trim();

        // Vérification des champs
        if (nom.isEmpty() || email.isEmpty() || ville.isEmpty() || niveau.isEmpty()) {
            JOptionPane.showMessageDialog(this, 
                "Veuillez remplir tous les champs !", 
                "Erreur", 
                JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Création objet
        Candidat c = new Candidat(nom, email, ville, niveau);

        // Appel DAO
        CandidatDao dao = new CandidatDao();
        dao.insert(c);

        JOptionPane.showMessageDialog(this, 
            "Candidat ajouté avec succès !", 
            "Succès", 
            JOptionPane.INFORMATION_MESSAGE);

        // Rafraîchir la table (si tu as une méthode loadTable())
        loadTableCandidat();

        // Vider les champs
        txtNom.setText("");
        txtEmail.setText("");
        txtVille.setText("");
        txtNiveau.setText("");

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, 
            "Erreur : " + e.getMessage(), 
            "Erreur", 
            JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnAjouterActionPerformed

