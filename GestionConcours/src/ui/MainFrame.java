    private void comboFiltreFiliereActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboFiltreFiliereActionPerformed
if (comboFiltreFiliere.getSelectedItem() == null) {
        return;
    }

    String filiereSelectionnee = comboFiltreFiliere.getSelectedItem().toString();
    ConcoursDao concoursDao = new ConcoursDao();
    DefaultTableModel model = (DefaultTableModel) tableConcours.getModel();
    model.setRowCount(0); // On vide le tableau avant de le remplir

    List<Concours> liste;
    
    // Logique de filtrage
    if (filiereSelectionnee.equals("Toutes")) {
        liste = concoursDao.findAll(); // Utilise votre méthode findAll
    } else {
        liste = concoursDao.getByFiliere(filiereSelectionnee); // Utilise votre nouvelle méthode
    }

    // Remplissage de la table
    for (Concours c : liste) {
        model.addRow(new Object[]{
            c.getId(),
            c.getTitre(),
            c.getDate(),
            c.getFiliere(),
            c.getCapacite(),
            c.getSeuil()
        });
    }
    }//GEN-LAST:event_comboFiltreFiliereActionPerformed

    private void comboFiltreVilleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboFiltreVilleActionPerformed
                                             
    // 1. Récupérer la ville sélectionnée
    if (comboFiltreVille.getSelectedItem() == null) return;
    String villeSelectionnee = comboFiltreVille.getSelectedItem().toString();
    
    // 2. Préparer le DAO et la liste
    CandidatDao dao = new CandidatDao();
    List<model.Candidat> liste;

    // 3. Charger les données selon le choix
    if (villeSelectionnee.equals("Toutes les villes") || villeSelectionnee.equals("")) {
        liste = dao.getAll();
    } else {
        liste = dao.getByVille(villeSelectionnee);
    }

    // 4. Mettre à jour le tableau JTable
    DefaultTableModel model = (DefaultTableModel) tableCandidat.getModel();
    model.setRowCount(0); // Vider le tableau

    for (model.Candidat c : liste) {
        model.addRow(new Object[]{
            c.getId(), 
            c.getNom(), 
            c.getEmail(), 
            c.getVille(), 
            c.getNiveauEtude()
        });
    }

    }//GEN-LAST:event_comboFiltreVilleActionPerformed

