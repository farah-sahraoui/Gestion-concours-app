    private void loadCombos() {

    CandidatDao candidatDao = new CandidatDao();
    ConcoursDao concoursDao = new ConcoursDao();

    comboCandidat.removeAllItems();
    comboConcours.removeAllItems();
    java.util.Set<String> filieresUniques = new java.util.HashSet<>();
    java.util.Set<String> villesUniques = new java.util.HashSet<>();
    
    comboFiltreVille.removeAllItems();
    comboFiltreVille.addItem("Toutes les villes");

    // Ajout du filtre par filière
    if (comboFiltreFiliere != null) {
        comboFiltreFiliere.removeAllItems();
        comboFiltreFiliere.addItem("Toutes");
    }

    for (Candidat c : candidatDao.getAll()) {
        comboCandidat.addItem(c);
    }

    for (Concours c : concoursDao.getAll()) {
        comboConcours.addItem(c);
        if (c.getFiliere() != null && !c.getFiliere().isEmpty()) {
            filieresUniques.add(c.getFiliere());
        }
        
    }
    for (model.Candidat c : candidatDao.getAll()) {
        if (c.getVille() != null && !c.getVille().trim().isEmpty()) {
            villesUniques.add(c.getVille());
        }
    }
    for (String ville : villesUniques) {
        comboFiltreVille.addItem(ville);
    }
    
    if (comboFiltreFiliere != null) {
        for (String f : filieresUniques) {
            comboFiltreFiliere.addItem(f);
        }
    }
}
  
