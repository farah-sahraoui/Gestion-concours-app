private void loadTableResultat() {
    ResultatDao rDao = new ResultatDao();
    
    
    // 1. Récupération des filtres
    String recherche = txtRecherche.getText().trim();
    String decision = (comboDecisionFilter.getSelectedItem() != null) ? 
                        comboDecisionFilter.getSelectedItem().toString() : "Tous";

    // 2. Récupération des données (via notre nouvelle méthode DTO)
    List<Object[]> liste = rDao.findFilteredWithNames(recherche, decision);

    // 3. Mise à jour du tableau
    DefaultTableModel model = (DefaultTableModel) tableResultat.getModel();
    model.setRowCount(0); // On vide tout avant de remplir

    for (Object[] ligne : liste) {
        // L'ordre ici doit être IDENTIQUE à l'étape 2
        model.addRow(ligne); 
    }
}
