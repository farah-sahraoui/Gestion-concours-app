    private void btnAjouterConcoursActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouterConcoursActionPerformed
    try {

    String titre = txtTitre.getText();
    String filiere = txtFiliere.getText();
    int capacite = Integer.parseInt(txtCapacite.getText());
    double seuil = Double.parseDouble(txtSeuil.getText());

    String dateText = txtDate.getText();

    java.util.Date date = new SimpleDateFormat("yyyy-MM-dd").parse(dateText);

    Concours c = new Concours();
    c.setTitre(titre);
    c.setDate(date);
    c.setFiliere(filiere);
    c.setCapacite(capacite);
    c.setSeuil(seuil);

    ConcoursDao dao = new ConcoursDao();
    dao.insert(c);

    loadTableConcours();

} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Format date : yyyy-MM-dd");
}
    }//GEN-LAST:event_btnAjouterConcoursActionPerformed

