    private void loadTableCandidat() {
    try {
        CandidatDao dao = new CandidatDao();
        List<Candidat> liste = dao.getAll();   // Assure-toi que cette méthode existe

        DefaultTableModel model = (DefaultTableModel) tableCandidat.getModel();
        model.setRowCount(0); // vider table
        tableCandidat.setSelectionMode(javax.swing.ListSelectionModel.SINGLE_SELECTION);
tableCandidat.getTableHeader().setReorderingAllowed(false); // Empêche de déplacer les colonnes

        for (Candidat c : liste) {
            model.addRow(new Object[]{
                c.getId(),
                c.getNom(),
                c.getEmail(),
                c.getVille(),
                c.getNiveauEtude()
            });
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this,
                "Erreur chargement table : " + e.getMessage());
    }
}
